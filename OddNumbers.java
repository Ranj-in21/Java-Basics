package javaAssignments.week1;

public class OddNumbers {
	
	public static void main(String[] args) {
		int i, num;
		System.out.println("Printing only odd integers");
	
		for(i=0; i<20; i++)
		{
			num = i+1;
			
			if(num%2!=0) {
				System.out.println(num);
			
		}
			
		}
		
	}

}
